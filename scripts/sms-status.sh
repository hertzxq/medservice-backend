#!/usr/bin/env bash
#
# Проверяет реальный статус ДОСТАВКИ сообщения(ий) sms.ru по sms_id.
# sms_id печатается в ответе при отправке (поле smsId).
#
# Usage:  ./scripts/sms-status.sh 202623-1000007[,другой_id...]
#
# Коды доставки sms.ru: 100 в очереди · 101 передаётся оператору · 102 отправлено
# (ждём отчёт) · 103 ДОСТАВЛЕНО · 104 не доставлено (истёк срок) · 105 удалено
# оператором · 106 сбой в телефоне · 107 неизвестная ошибка · 108 ОТКЛОНЕНО
# (часто = имя отправителя не одобрено оператором) · 110 спам.
set -euo pipefail
[ $# -ge 1 ] || { echo "usage: $0 <sms_id[,sms_id...]>"; exit 1; }
DIR="$(cd "$(dirname "$0")/.." && pwd)"
API="$(grep -E '^SMS_RU_API_ID=' "$DIR/.env" | cut -d= -f2-)"
curl -s "https://sms.ru/sms/status?api_id=${API}&sms_id=${1}&json=1" | python3 -m json.tool
